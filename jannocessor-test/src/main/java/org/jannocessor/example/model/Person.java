package org.jannocessor.example.model;

import java.util.Date;

import org.jannocessor.common.annotation.BuilderModel;
import org.jannocessor.common.annotation.DtoIgnore;
import org.jannocessor.common.annotation.DtoModel;

@BuilderModel
@DtoModel
public class Person {

	private String firstName;

	private String lastName;

	@DtoIgnore
	private Date birthDate;

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public Date getBirthDate() {
		return birthDate;
	}

	public void setBirthDate(Date birthDate) {
		this.birthDate = birthDate;
	}

	@Override
	public String toString() {
		return "Person [firstName=" + firstName + ", lastName=" + lastName + ", birthDate="
				+ birthDate + "]";
	}

}
